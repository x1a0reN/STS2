function Copy-FrierenAssetDir([int]$ExpectedPngCount, [string]$TargetName) {
  $sourcePath = @(Get-ChildItem -LiteralPath $assetRoot -Directory | Where-Object {
    @(Get-ChildItem -LiteralPath $_.FullName -Recurse -File -Filter "*.png").Count -eq $ExpectedPngCount
  } | Select-Object -First 1 -ExpandProperty FullName)[0]

  if (-not (Test-Path -LiteralPath $sourcePath)) {
    throw "Frieren asset source missing: $sourcePath"
  }

  $targetPath = Join-Path (Join-Path $modsRoot "assets\frieren") $TargetName
  if (-not (Test-Path -LiteralPath $targetPath)) {
    New-Item -ItemType Directory -Path $targetPath -Force | Out-Null
  }

  Copy-Item -Path (Join-Path $sourcePath "*") -Destination $targetPath -Recurse -Force
}

function Copy-FrierenCharacterAsset([string]$SourcePath, [string]$TargetName) {
  $sourcePath = $SourcePath
  if (-not (Test-Path -LiteralPath $sourcePath)) {
    throw "Frieren character asset missing: $sourcePath"
  }

  $targetPaths = @(
    (Join-Path $modsRoot "assets\frieren\character"),
    (Join-Path $legacyModDir "assets\frieren\character")
  )

  foreach ($targetPath in $targetPaths) {
    if (-not (Test-Path -LiteralPath $targetPath)) {
      New-Item -ItemType Directory -Path $targetPath -Force | Out-Null
    }

    Copy-Item -LiteralPath $sourcePath -Destination (Join-Path $targetPath $TargetName) -Force
  }
}

