function Write-DummyModManifest([string]$Path, [string]$Id, [string]$Name) {
  $manifest = [ordered]@{
    id = $Id
    name = $Name
    author = "x1a0reN"
    description = "Compatibility marker generated by GongDou installer."
    version = $frierenManifest.version
    has_pck = $false
    has_dll = $false
    affects_gameplay = $false
  }
  $json = $manifest | ConvertTo-Json -Depth 4
  Set-Content -LiteralPath $Path -Value $json -Encoding UTF8
}

function Disable-LegacyAssetJsonManifests([string]$AssetPath) {
  if (-not (Test-Path -LiteralPath $AssetPath)) {
    return
  }

  $root = [System.IO.Path]::GetFullPath($AssetPath).TrimEnd('\', '/') + [System.IO.Path]::DirectorySeparatorChar
  Get-ChildItem -LiteralPath $AssetPath -Recurse -File -Filter "*.json" | ForEach-Object {
    $file = [System.IO.Path]::GetFullPath($_.FullName)
    if (-not $file.StartsWith($root, [System.StringComparison]::OrdinalIgnoreCase)) {
      throw "Refusing to disable JSON outside Frieren asset root: $file"
    }

    $target = "$file.gongdou-disabled"
    $index = 1
    while (Test-Path -LiteralPath $target) {
      $target = "$file.gongdou-disabled-$index"
      $index += 1
    }

    Rename-Item -LiteralPath $file -NewName ([System.IO.Path]::GetFileName($target)) -Force
  }
}

